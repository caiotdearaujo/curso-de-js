/**
 * OPERADORES
 * 
 * ARITMÉTICOS
 * + soma/concatenação
 * - subtração
 * * multiplicação
 * / divisão
 * ** potenciação
 * % módulo da divisão
 * 
 * ATRIBUIÇÃO
 * = atribui valor à variável
 * 
 * INCREMENTO (i = i+1)
 * i++ (operação ocorre após/pós-incremento) 
 * ++i (operação ocorre primeiro/pré-incremento)
 * 
 * DECREMENTO (i = i-1)
 * i-- (operação ocorre após/pós-decremento)
 * --i (operação ocorre primeiro/pré-decremento)
 * 
 * ATRIBUIÇÃO COM OPERAÇÃO
 * x += y // x = x+y
 * x -= y // x = x-y
 * x *= y // x = x*y
 * x /= y // x = x/y
 * x **= y // x = x**y
 * x %= y // x = x%y
 */